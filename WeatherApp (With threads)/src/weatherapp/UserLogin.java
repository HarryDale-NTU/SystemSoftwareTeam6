    public void UserVerify(String username, String password, String filepath) {
        boolean isLocated = false; //Set check for located file as false
        String userTemp = "";//Declared empty Variable for password
        String passTemp = "";//Declared empty Variable for password
           
        try {
            x = new Scanner(new File(filepath));
            x.useDelimiter("[,\n]");//remove , from the string inside file
            while (x.hasNext() && !isLocated) {
                userTemp = x.next();
                passTemp = x.next();

                if (userTemp.trim().equals(username.trim()) && passTemp.trim().equals(password.trim())) {
                    isLocated = true;
                }
            }
            if (isLocated == true) {
                this.dispose();//close form
                new UserClient().setVisible(true);//Open Client Form
                
                DatagramSocket client = new DatagramSocket();
                String loginSuc = "\nUser : " + txtUser.getText().trim() + " Has logged in \t";//Produce a string with the entered user name
                byte[] data = loginSuc.getBytes();
                InetAddress addr = InetAddress.getByName("localhost");
                DatagramPacket sendPacket = new DatagramPacket (data,data.length,addr,1201);
                client.send(sendPacket);
               

            } else {
                JOptionPane.showMessageDialog(null, "User Not Found or password incorrect");//If user is not found display error message
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");//Catch any error and display to user
        }

    }
